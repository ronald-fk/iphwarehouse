$rows = $result->fetch_all(MYSQLI_ASSOC);

// Function to format currency
function formatRupiah($value) {
    return 'Rp ' . number_format($value, 0, ',', '.') . ',-';
}

// Function to format date
function formatTanggal($tanggal) {
    setlocale(LC_TIME, 'id_ID.utf8');
    $date = new DateTime($tanggal);
    return strftime('%d %B %Y', $date->getTimestamp());
}

// Generate PDF
$mpdf = new \Mpdf\Mpdf();
$html = '<h1>Report Incoming</h1>';

$html .= '<table border="1" cellpadding="5" cellspacing="0" style="width: 100%;">';
$html .= '<thead><tr>';
foreach ($columns as $col) {
    $html .= '<th>' . ucfirst(str_replace('_', ' ', $col)) . '</th>';
}
$html .= '</tr></thead>';
$html .= '<tbody>';

$grandTotal = 0;
foreach ($rows as $row) {
    $html .= '<tr>';
    foreach ($columns as $col) {
        if ($col == 'harga_cost' || $col == 'subtotal') {
            $html .= '<td>' . formatRupiah($row[$col]) . '</td>';
            if ($col == 'subtotal') {
                $grandTotal += $row[$col];
            }
        } elseif ($col == 'tanggal') {
            $html .= '<td>' . formatTanggal($row[$col]) . '</td>';
        } else {
            $html .= '<td>' . $row[$col] . '</td>';
        }
    }
    $html .= '</tr>';
}

$html .= '</tbody></table>';
$html .= '<p><strong>Grand Total: ' . formatRupiah($grandTotal) . '</strong></p>';

// // Tambahkan nama user di kanan bawah
