session_start();
require 'vendor/autoload.php'; // Include Composer's autoload file
include('config.php');

$columns = isset($_GET['columns']) ? explode(',', $_GET['columns']) : [];
$month = isset($_GET['month']) ? $_GET['month'] : '';
$year = isset($_GET['year']) ? $_GET['year'] : '';
$user = isset($_SESSION['username']) ? $_SESSION['username'] : 'Guest';

if (empty($columns)) {
    die('No columns selected');
}

// Prepare SQL query based on selected columns
$columnMap = [
    'tanggal' => 'i.tanggal',
    'nomor_ref' => 'i.nomor_ref',
    'kode_buku' => 'i.kode_buku',
    'no_job' => 'p.no_job',
    'nama_buku' => 'p.nama_buku',
    'kuantitas' => 'i.kuantitas',
    'harga_cost' => 'p.harga_cost',
    'subtotal' => '(i.kuantitas * p.harga_cost) AS subtotal'
];

$selectedColumns = array_map(function ($col) use ($columnMap) {
    return isset($columnMap[$col]) ? $columnMap[$col] : $col;
}, $columns);

$sql = "SELECT " . implode(', ', $selectedColumns) . " 
        FROM incoming i
        LEFT JOIN products p ON i.kode_buku = p.kode_buku";

if ($month && $year) {
    $sql .= " WHERE MONTH(i.tanggal) = '$month' AND YEAR(i.tanggal) = '$year'";
} elseif ($year) {
    $sql .= " WHERE YEAR(i.tanggal) = '$year'";
}

